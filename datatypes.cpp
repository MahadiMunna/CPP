#include <iostream>
using namespace std;

int main()
{
    long long int a = 12;
    cout << "size of int " << sizeof(a) << endl;

    float b;
    cout << "size of float " << sizeof(b) << endl;

    long double c;
    cout << "size of double " << sizeof(c) << endl;

    char d;
    cout << "size of char " << sizeof(d) << endl;
}